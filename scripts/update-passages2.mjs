async function main() {
  for (const [date, story] of Object.entries(stories)) {
    const escaped = story.replace(/'/g, "''");
    const sql = `UPDATE daily_quizzes SET passage = '${escaped}' WHERE date = '${date}' AND tag = '阅读理解';`;
    const tmpFile = `/tmp/passage-${date}.sql`;
    writeFileSync(tmpFile, sql);
    try {
      execSync(
        `cd ~/Workspaces/gongzu && npx wrangler d1 execute gongzu --remote --file ${tmpFile}`,
        { encoding: 'utf8', timeout: 15000 }
      );
      console.log(`✅ ${date}: updated (${story.length} chars)`);
    } catch (e) {
      console.error(`❌ ${date}: ${e.message.slice(0, 100)}`);
    }
  }
}

main().catch(console.error);
