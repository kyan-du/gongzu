async function main() {
  for (const [date, { name, passage }] of Object.entries(stories)) {
    const escaped = passage.replace(/'/g, "''");
    const sql = `UPDATE daily_quizzes SET passage = '${escaped}' WHERE date = '${date}' AND tag = '阅读理解';`;
    const tmpFile = `/tmp/passage-annotate-${date}.sql`;
    writeFileSync(tmpFile, sql);
    try {
      const out = execSync(
        `cd ~/Workspaces/gongzu && npx wrangler d1 execute gongzu --remote --file ${tmpFile}`,
        { encoding: 'utf8', timeout: 15000 }
      );
      const changesMatch = out.match(/"changes":\s*(\d+)/);
      const changes = changesMatch ? changesMatch[1] : '?';
      console.log(`✅ ${date} ${name}: annotated (${passage.length} chars, changes=${changes})`);
    } catch (e) {
      console.error(`❌ ${date} ${name}: ${e.message.slice(0, 200)}`);
    } finally {
      try { unlinkSync(tmpFile); } catch {}
    }
  }
}

main().catch(console.error);
